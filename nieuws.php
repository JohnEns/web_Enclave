                            <h2>Samenvatting</h2>
                            <p>In de eerste aflevering van Engine Room leggen Julius en Bryann van Image &amp; Form Games uit hoe de steambots zijn gecre&euml;erd en hoe zij onbedoeld de wereld overnemen. Het alternatieve universum van SteamWorld gaat uit van het idee dat Charles Babbage zijn stoomcomputer toch heeft gebouwd in de 19<sup>e</sup> eeuw. Deze computer werd een groot succes en al snel werden er steambots ontwikkeld. Er volgen oorlogen en het restant van de mensheid die dat overleefd, waren het bombarderen niet waard.</p>
                            <h2>Programma volgen</h2><p>De Nintendo DS-game <em>SteamWorld Tower Defense</em> vindt plaats na de grote oorlogen. De robots gaan gewoon door met het uitvoeren van hun instructies en mijnen de wereld leeg. Het <a href="http://frontpage.fok.nl/nieuws/638796/1/1/50/steamworld-dig-naar-ps4-en-ps-vita.html">grote succes</a> van Image &amp; Form Games is uiteraard het platform-avontuur <em>SteamWorld Dig</em>, waar Rusty de Robot de mijn van zijn oom Joe ontgint en veel vreemde zaken aantreft.</p>
                            <h2>Ruimtereizen</h2><p>Een aantal eeuwen na <em>SteamWorld Dig</em> is er een enorme ramp en de robots trekken de ruimte in. In de turnbased strategy <em>SteamWorld Heist</em> leidt Captain Piper haar team door werelden, neemt ze ruimteschepen over en zoekt ze mooie hoeden. De meeste missies zijn verhaalgerelateerd, maar er zijn ook zij-missies.</p>

                            <h2>Toekomst</h2><p>Image &amp; Form Games komen met meer games in het SteamWorld-universum. Wat voor games en wanneer willen ze nog niet laten weten.</p>
                            <p><em>Steamworld Heist</em> komt binnenkort uit op Nintendo 3DS, gevolgd door PlayStation 4, PlayStation Vita, Xbox One, Wii U, pc, Android en iOS.</p>

