                            <h2>Samenvatting</h2>
                            De Friese actie avonturengame <em>Cross of the Dutchman</em> laat je de oude legende van Grutte Pier Donia herleven. Je bent een boer die zich genoodzaakt ziet de strijd aan te gaan met de Hollanders en de Saksen. Volgens de legende was Pier bijzonder groot en sterk en Triangle Studios springt daar graag op in. Pier verzamelt een rebellenleger om zich heen en brengt de Friese vijanden grote schade toe. De Hollanders noemden hem 'Kruis der Hollanders' en gaven zo indirect de naam aan deze game.
                            <h2>Strijd en Tegenslag</h2><p>Triangle Studios heeft veel pech en tegenslag gehad bij het maken van het spel. Na zes jaar ploeteren is het eindelijk af. <em>Cross of the Dutchman</em> verschijnt precies 500 jaar na de gebeurtenissen in de game. De game is te spelen in het Fries, Nederlands, Engels en nog een handvol talen. Er wordt nu al gehint op een tweede deel. Als je wilt weten hoe het spel afloopt, dan kun je altijd even op Wikipedia spieken.</p>
                            <p><em>Cross of the Dutchman</em> is verkrijgbaar voor Mac en pc via onder andere Steam, Humble Store, Green Man Gaming, Gamesrocket, Nuveem en GetGamesGo.</p>


